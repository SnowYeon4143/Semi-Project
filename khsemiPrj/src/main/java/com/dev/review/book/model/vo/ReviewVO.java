package com.dev.review.book.model.vo;

public class ReviewVO 
{
	String bookReviewNumber;
	String bookReviewTitle;
	String bookReviewContext;
	String bookReviewReportCheck;
	String bookReviewLikeCount;
	String bookReviewDate;
	String deleteCheck;
	
	public String getBookReviewNumber() {
		return bookReviewNumber;
	}
	public void setBookReviewNumber(String bookReviewNumber) {
		this.bookReviewNumber = bookReviewNumber;
	}
	public String getBookReviewTitle() {
		return bookReviewTitle;
	}
	public void setBookReviewTitle(String bookReviewTitle) {
		this.bookReviewTitle = bookReviewTitle;
	}
	public String getBookReviewContext() {
		return bookReviewContext;
	}
	public void setBookReviewContext(String bookReviewContext) {
		this.bookReviewContext = bookReviewContext;
	}
	public String getBookReviewReportCheck() {
		return bookReviewReportCheck;
	}
	public void setBookReviewReportCheck(String bookReviewReportCheck) {
		this.bookReviewReportCheck = bookReviewReportCheck;
	}
	public String getBookReviewLikeCount() {
		return bookReviewLikeCount;
	}
	public void setBookReviewLikeCount(String bookReviewLikeCount) {
		this.bookReviewLikeCount = bookReviewLikeCount;
	}
	public String getBookReviewDate() {
		return bookReviewDate;
	}
	public void setBookReviewDate(String bookReviewDate) {
		this.bookReviewDate = bookReviewDate;
	}
	public String getDeleteCheck() {
		return deleteCheck;
	}
	public void setDeleteCheck(String deleteCheck) {
		this.deleteCheck = deleteCheck;
	}

}
